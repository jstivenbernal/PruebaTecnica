CREATE TABLE Facturas (
    IdFactura NUMBER GENERATED BY DEFAULT AS IDENTITY,
    NumeroFactura VARCHAR(50) NOT NULL,
    Fecha DATE NOT NULL,
    TipodePago VARCHAR(20),
    DocumentoCliente VARCHAR(50),
    NombreCliente VARCHAR(100),
    Subtotal FLOAT,
    Descuento FLOAT,
    IVA FLOAT,
    TotalDescuento FLOAT,
    TotalImpuesto FLOAT,
    Total FLOAT,
    PRIMARY KEY(IdFactura)
);

CREATE TABLE Productos(
    IdProducto NUMBER GENERATED BY DEFAULT AS IDENTITY,
    Producto VARCHAR(50) NOT NULL,
    PRIMARY KEY(IdProducto)
);

CREATE TABLE FacturasProductos(
    IdFacturaProducto NUMBER GENERATED BY DEFAULT AS IDENTITY,
    IdFactura NUMBER NOT NULL,
    IdProducto NUMBER NOT NULL,
    FOREIGN KEY(IdFactura) REFERENCES Facturas(IdFactura),
    FOREIGN KEY(IdProducto) REFERENCES Productos(IdProducto)
);

